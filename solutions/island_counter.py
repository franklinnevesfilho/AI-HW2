def _bfs(grid, i, j):
    queue = [(i, j)]
    while queue:
        i, j = queue.pop(0)
        if i < 0 or i >= len(grid) or j < 0 or j >= len(grid[0]) or grid[i][j] == 0:
            continue
        grid[i][j] = 0
        queue.append((i + 1, j))
        queue.append((i - 1, j))
        queue.append((i, j + 1))
        queue.append((i, j - 1))


def _dfs(grid, i, j):
    if i < 0 or i >= len(grid) or j < 0 or j >= len(grid[0]) or grid[i][j] == 0:
        return
    grid[i][j] = 0
    _dfs(grid, i + 1, j)
    _dfs(grid, i - 1, j)
    _dfs(grid, i, j + 1)
    _dfs(grid, i, j - 1)



def island_count(grid=None, method='dfs'):
    if grid is None:
        # make copy of island_map
        grid = [row.copy() for row in island_map]

    if method not in ['dfs', 'bfs']:
        raise ValueError("Method must be either 'dfs' or 'bfs'")

    count = 0
    for i in range(len(grid)):
        for j in range(len(grid[0])):
            if grid[i][j] == 1:
                count += 1
                if method == 'dfs':
                    _dfs(grid, i, j)
                elif method == 'bfs':
                    _bfs(grid, i, j)

    return count
